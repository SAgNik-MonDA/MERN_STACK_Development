// trim method
let str="              sagnik ";
console.log(str.trim());

// touppercase method
let a="sagnik";
console.log(a.toUpperCase());

// tolowercase method
let string="SAGNIK";
console.log(string.toLowerCase());

// string methods with arguments
let msg="i love coding";
console.log(msg.indexOf("love"));
console.log(msg.indexOf("k"));
console.log(msg.indexOf("c"));

// method chaining
let name="                   sagnik          ";
let newname=name.trim().toUpperCase();
console.log(newname);

// slice method
let b="hellow";
console.log(b.slice(0,3));
console.log(b.slice(-2));

// replace method
let c="i love coding";
console.log(c.replace("love" , "do" ))

// repeat method
console.log(c.repeat(3));


